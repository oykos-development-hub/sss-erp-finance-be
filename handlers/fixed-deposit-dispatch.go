package handlers

import (
	"context"
	"net/http"
	"strconv"

	"gitlab.sudovi.me/erp/finance-api/contextutil"
	"gitlab.sudovi.me/erp/finance-api/dto"
	"gitlab.sudovi.me/erp/finance-api/errors"
	"gitlab.sudovi.me/erp/finance-api/services"

	"github.com/go-chi/chi/v5"
	"github.com/oykos-development-hub/celeritas"
)

// FixedDepositDispatchHandler is a concrete type that implements FixedDepositDispatchHandler
type fixeddepositdispatchHandlerImpl struct {
	App     *celeritas.Celeritas
	service services.FixedDepositDispatchService
}

// NewFixedDepositDispatchHandler initializes a new FixedDepositDispatchHandler with its dependencies
func NewFixedDepositDispatchHandler(app *celeritas.Celeritas, fixeddepositdispatchService services.FixedDepositDispatchService) FixedDepositDispatchHandler {
	return &fixeddepositdispatchHandlerImpl{
		App:     app,
		service: fixeddepositdispatchService,
	}
}

func (h *fixeddepositdispatchHandlerImpl) CreateFixedDepositDispatch(w http.ResponseWriter, r *http.Request) {
	var input dto.FixedDepositDispatchDTO
	err := h.App.ReadJSON(w, r, &input)
	if err != nil {
		_ = h.App.WriteErrorResponse(w, http.StatusBadRequest, err)
		return
	}

	validator := h.App.Validator().ValidateStruct(&input)
	if !validator.Valid() {
		_ = h.App.WriteErrorResponseWithData(w, errors.MapErrorToStatusCode(errors.ErrBadRequest), errors.ErrBadRequest, validator.Errors)
		return
	}

	userIDString := r.Header.Get("UserID")

	userID, err := strconv.Atoi(userIDString)

	if err != nil {
		_ = h.App.WriteErrorResponse(w, errors.MapErrorToStatusCode(errors.ErrUnauthorized), errors.ErrUnauthorized)
		return
	}

	ctx := context.Background()
	ctx = contextutil.SetUserIDInContext(ctx, userID)

	res, err := h.service.CreateFixedDepositDispatch(ctx, input)
	if err != nil {
		_ = h.App.WriteErrorResponse(w, errors.MapErrorToStatusCode(err), err)
		return
	}

	_ = h.App.WriteDataResponse(w, http.StatusOK, "FixedDepositDispatch created successfuly", res)
}

